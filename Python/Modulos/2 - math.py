import math
# 1 - acessar o Número Pi
print(math.pi)
print(f"Valor de Pi: {math.pi:.2f}")

# 2 - acessar o Número de Euler
print(math.e)
print(f"Valor de Euler: {math.e:.2f}")



#  - arredondar um número para cima e baixo
print(math.ceil(4.2))  # arredonda para cima
print(math.floor(4.7)) # arredonda para baixo
print(round(4.5))      # arredonda para o inteiro mais próximo
